bottles = 10

while bottles > 1:
    print(bottles, "green bottles hanging on the wall,")
    print(bottles, "green bottles hanging on the wall,")
    print("and if one green bottle should accidentally fall,")
    print("they'll be", bottles-1, "green bottles hanging on the wall.")
    print()
    bottles = bottles -1

